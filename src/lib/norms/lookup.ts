// Lineare Interpolation zwischen Stützpunkten
export function interpolateT(samples: [number,number][], summe: number): number {
  if (summe <= samples[0][0]) return samples[0][1]
  if (summe >= samples[samples.length-1][0]) return samples[samples.length-1][1]
  for (let i = 0; i < samples.length - 1; i++) {
    const [x0,y0] = samples[i]
    const [x1,y1] = samples[i+1]
    if (summe >= x0 && summe <= x1) {
      if (x1 === x0) return y0
      return Math.round(y0 + (y1 - y0) * (summe - x0) / (x1 - x0))
    }
  }
  return samples[samples.length-1][1]
}

